    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
      int confirm = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

if (confirm == JOptionPane.YES_OPTION) {
    try {
        String sql = "DELETE FROM menu WHERE id_menu=?";
        Connection conn = dbkoneksi.getKoneksi();
        PreparedStatement pst = conn.prepareStatement(sql);
        
        pst.setString(1, jTextField1.getText()); // ID Menu dari form
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
        
        loadData();
        kosongkanForm();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menghapus: " + e.getMessage());
    }
}
    }//GEN-LAST:event_jButton3ActionPerformed

