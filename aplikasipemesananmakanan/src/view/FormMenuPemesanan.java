    // METHOD UNTUK MEMUAT DAN MENAMPILKAN DATA DARI DATABASE KE JTABLE
    private void loadData() {
        model = new DefaultTableModel();
        model.addColumn("ID Menu");
        model.addColumn("Nama Menu");
        model.addColumn("Kategori");
        model.addColumn("Harga");
        model.addColumn("Stok");
        jTable1.setModel(model); // Menggunakan nama variabel jTable1

        try {
            String sql = "SELECT * FROM menu";
            // Memanggil metode getKoneksi() yang benar dari kelas dbkoneksi
            Connection conn = dbkoneksi.getKoneksi(); 
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery(sql);

            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString("id_menu"),
                    res.getString("nama_menu"),
                    res.getString("kategori"),
                    res.getBigDecimal("harga"),
                    res.getInt("stok")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }

    // METHOD UNTUK MENGOSONGKAN FORM INPUT
    private void kosongkanForm() {
        jTextField1.setText(null); // ID Menu
        jTextField2.setText(null); // Nama Menu
        jTextField3.setText(null); // Harga
        jComboBox1.setSelectedIndex(0); // Kategori
        jTextField4.setText(null); // Stok
    }

    // METHOD UNTUK MENONAKTIFKAN INPUT ID MENU
    private void nonaktifkanIdMenu() {
        jTextField1.setEnabled(false); // ID Menu tidak bisa diisi manual
       
   }
            
            
            
    

   




